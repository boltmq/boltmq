// GetVersionDesc 获得版本号对应的 描述信息
// Author: tianyuliang, <tianyuliang@gome.com.cn>
// Since: 2017/9/4
func GetVersionDesc(value int) string {
	start := MQVersion.V3_0_0_SNAPSHOT
	end := MQVersion.V3_5_9
	for i := start; i <= end; i++ {
		if i == value {
			return ""
		}
	}

	return "HigherVersion"
}
